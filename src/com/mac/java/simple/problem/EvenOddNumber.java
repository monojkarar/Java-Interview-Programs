package com.mac.java.simple.problem;

/**
 * 
 * Find a number if it is a Odd or Even Number.
 * 
 * @author monojkarar
 *
 */
public class EvenOddNumber {

	public static void main(String[] args) {

		int n = 1;

		if (n % 2 == 0) {
			System.out.println("Even Number.");
		} else {
			System.out.println("Odd Number");
		}

	}

}
